<script>
// 获取当前小程序信息
const accountInfo = uni.getAccountInfoSync(); 
import { mapGetters, mapActions, mapMutations } from 'vuex'
const { $Message } = require('@/wxcomponents/base/index');
import { setUserInfo, getUserInfo, getCode } from '@/util/api/user.js'
import { getTextImageList } from '@/util/api/order.js'
    export default {
        computed:{
			...mapGetters('user',[
                'get_phone',
                'get_nickName',
                'get_headimgUrl',
                'get_appId',
                'get_enterpriseId',
                'get_code'
			]),
        },
        onLoad(options) {
            uni.setNavigationBarColor({
                frontColor: '#000000',
                backgroundColor: '#ffffff',
                animation: {
                    duration: 400,
                    timingFunc: 'easeIn'
                }
            })
        },
        onShow() {
            // 路由返回触发更新
            //this.getUserInfoAPI()
        },
        data(){
            return{
                jsCode:null, //存储登陆code
                userCode:'', // 用户信息
                imgList: []
            }
        },
        mounted(){
            // 获取用户
            //this.getUserInfoAPI()
            this.login()
            this.getTextImageList()
        },
        methods:{
            ...mapActions('user',[
                'act_nickName',
                'act_code'
            ]),
            ...mapMutations('user',[
                'mut_outCode'
            ]),
            toWebView (_) {
                uni.navigateTo({ 
                    url: `/pages/tabBar/my/components/web_view?url=${_.link}&title=${_.title}` 
                })
            },
            // 获得图文列
            getTextImageList () {
                let params = {
                    appId: this.get_appId,
                    enterpriseId: this.get_enterpriseId
                }
                getTextImageList(params).then(res => {
                    console.log('图文', res)
                    this.imgList = res.data.data
                })
            },
            // 获取code值
            login(){
                uni.login({
                    provider: 'weixin',
                    success:(res) => {
                        // 获取code值
                        let param = {
                            jsCode: res.code,
                            authorizerAppid:  accountInfo.miniProgram.appId
                        }
                        this.jsCode = param
                        console.log('获取了code值',this.jsCode)
                    }
                });
            },

            // 获取登陆信息
            getCode(param){
                getCode(param).then(res=>{
                    let code = res.data.data
                    // 存储 
                    this.act_code(code)
                    // 存储本地
                    uni.setStorage({
                        key: 'code',
                        data: code
                    })
                    console.log(code)
                    setUserInfo(this.userCode).then(res=>{
                        this.getUserInfoAPI('1')
                    })
                })
            },
            
            // 获取用户信息AIP
            getUserInfoAPI(flag){
                getUserInfo().then(res=>{
                    let {headimgUrl,nickName,phone,sex,birthday,province,city,area,id} = res.data.data
                    this.act_nickName({headimgUrl,nickName,phone,sex,birthday,province,city,area,id})
                    if(flag){
                        this.onClickUserInfo()
                    }
                })
            },
            // 获取用户信息
            getUserInfo(val){
                let param = val.detail
                this.userCode = val.detail
                
                if(param.encryptedData){
                    uni.checkSession({
                        success:res=>{
                            console.log('登陆还在')
                            if(this.get_nickName){
                                this.onClickUserInfo()
                            }else{
                                // 去验证登陆是否过期
                                this.getCode(this.jsCode)
                            }
                        },
                        fail:err=>{
                            console.log('登陆失效')
                            //this.getCode(this.jsCode)
                            this.onClickUserInfo()
                        }
                    }) 
                }else{
                    $Message({
                        content:'取消授权',
                        type: 'error'
                    });
                }
            },
            onClikcPage (url) {
                uni.navigateTo({ 
                    url
                })
            },
            // 修改个人资料
            onClickUserInfo(){
                uni.navigateTo({ 
                    url: '/pages/tabBar/my/components/userInfo' 
                })
            },

            // 修改密码
            onClickPassword(){
                uni.navigateTo({ 
                    url: '/pages/tabBar/my/components/changePassword' 
                })
            },
            onClickFeedBack () {
                uni.navigateTo({
                    url:'/pages/tabBar/classify/myFeedBack'
                })
            },
            // 服务协议
            onClickService(type){
                uni.navigateTo({ 
                    url: `/pages/tabBar/my/components/serviceAgreement?type=${type}`
                })
            },
            // 退出登陆
            onClickOut(){
                console.log('退出')
                uni.removeStorage({
                    key: 'code',
                    success: (result) => {
                        clearInterval(getApp().globalData.time)
                        this.mut_outCode()
                        this.act_code(null)
                    },
                    fail: (error) => {}
                })
                
            }
        },
        // 监听cood
		watch:{
			get_code(val){
                if(!val) return
				this.getUserInfoAPI(false)
			}
		}
    }
</script>

